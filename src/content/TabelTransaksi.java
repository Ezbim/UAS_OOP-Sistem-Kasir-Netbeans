     public void loadData() {
        // Call methods to load data for each table
        loadTransaksiData();
        loadBarangTransaksiData();
        calculateTotal();
    }
     private void loadTransaksiData() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sistemkasir", "root", "");
             PreparedStatement pst = con.prepareStatement("SELECT * FROM transaksi");
             ResultSet rs = pst.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing data

            while (rs.next()) {
                // Retrieve data from the result set
                int idTransaksi = rs.getInt("id_transaksi");
                int total = rs.getInt("total");
                String createdAt = rs.getString("created_at");

                // Add a new row to the table
                model.addRow(new Object[]{idTransaksi, total, createdAt});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
     private void loadBarangTransaksiData() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/sistemkasir", "root", "");
             PreparedStatement pst = con.prepareStatement("SELECT * FROM barang_transaksi");
             ResultSet rs = pst.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0); // Clear existing data

            while (rs.next()) {
                // Retrieve data from the result set
                int idBarangTransaksi = rs.getInt("id_barang_transaksi");
                int idTransaksi = rs.getInt("id_transaksi");
                int idProduk = rs.getInt("id_produk");
                int harga = rs.getInt("harga");
                int jumlah = rs.getInt("jumlah");

                // Add a new row to the table
                model.addRow(new Object[]{idBarangTransaksi, idTransaksi, idProduk, harga, jumlah});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
     
     private void calculateTotal() {
        int totalSum = 0;

        // Iterate through jTable1 and sum up the "total" column
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            totalSum += (int) jTable1.getValueAt(i, 1); // Assuming "total" is in the second column (index 1)
        }

        // Set the calculated total in jLabel4
        jLabel4.setText(Integer.toString(totalSum));
    }
